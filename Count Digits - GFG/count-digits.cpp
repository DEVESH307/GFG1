class Solution{
public:
    int evenlyDivides(int n){
        //code here
        int ans = 0;
        int temp = n;
        while(temp){
            int last = temp%10;
            temp /= 10;
            if(last != 0 && n%last == 0) ans++;
        }
        return ans;
    }
};




